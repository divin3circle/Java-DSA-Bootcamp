public class Primitives {
    public static void main(String[] args) {
        //integers - primitive(data type that you cannot break into further data types
        int rollNumber = 64;
        //character primitive
        char c = 'c';
        //float primitive
        float average = 98.87f; //f is essential
        //double for large decimal numbers
        double largeDecimal = 87852.888888;
        //long - large integer values
        long largeInteger = 4754649688764646546L;
        //boolean true or false
        boolean isBool = true;

        //wrapper classes
        Integer rno = 64;
        rno.hashCode();
        Boolean isMe = true;
        isMe.notifyAll();



    }
}
